// while
let index = 0
while (index <= 10) {
    console.log(`The value of Index of ${index}`);
    index++
}

let myArray = ["Ironman", "superman", "batman"]

let arr = 0 

while (arr < myArray.length) {
    console.log(`The value is ${myArray[arr]}`)
    arr++
}

//==================================== Do-while ==========================================
// the loop will run atleast one time.

let i = 1
do {
    console.log(`The value is ${i}`);
    i++
} while (i <= 10);


let j = 11
do {
    console.log(`The value is ${j}`);
    j++
} while (j <= 10);